-- DWP Menu Rename: 역할 관리 → 권한 관리
-- 생성일: 2026-01-22
-- 목적: 메뉴명 "역할 관리"를 "권한 관리"로 변경
-- ========================================

-- ========================================
-- 1. com_resources 테이블 업데이트
-- ========================================
UPDATE com_resources
SET name = '권한 관리',
    updated_at = CURRENT_TIMESTAMP
WHERE tenant_id = 1
  AND type = 'MENU'
  AND key = 'menu.admin.roles';

-- ========================================
-- 2. sys_menus 테이블 업데이트
-- ========================================
UPDATE sys_menus
SET menu_name = '권한 관리',
    description = '권한 및 역할 관리',
    updated_at = CURRENT_TIMESTAMP
WHERE tenant_id = 1
  AND menu_key = 'menu.admin.roles';

-- ========================================
-- 변경 사항 요약
-- ========================================
-- 메뉴명 변경:
--   - com_resources: "역할 관리" → "권한 관리"
--   - sys_menus: "역할 관리" → "권한 관리"
--   - sys_menus.description: "역할 및 권한 관리" → "권한 및 역할 관리"
